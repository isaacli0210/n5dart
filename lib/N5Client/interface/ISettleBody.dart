abstract class ISettleBody {
  /// Terminal ID
  String tID;

  /// Settle Type
  String tYPE;

  /// Sale Count
  String sC;

  /// Sale Amount
  String sA;

  /// Void Sale Count
  String vSC;

  /// Void Sale Amount
  String vSA;

  /// Refund Count
  String rC;

  /// Refund Amount
  String rA;

  /// Void Refund Amount
  String vRC;

  /// Void Refund Amount
  String vRA;

  /// Offline Count
  String oC;

  /// Offline Amount
  String oA;

  /// Void Offline Count
  String vOC;

  /// Void Offline Amount
  String vOA;
}
